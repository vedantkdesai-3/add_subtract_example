package com.vmware.vmwareassigment.ro;

public class Input {

	private Double x;
	private Double y;

	public Input() {
	}

	public Input(Double x, Double y) {
		super();
		this.x = x;
		this.y = y;
	}

	public Double getX() {
		return x;
	}

	public void setX(Double x) {
		this.x = x;
	}

	public Double getY() {
		return y;
	}

	public void setY(Double y) {
		this.y = y;
	}

}
